package 객체지향_프로그래밍1;

public class MyMath {

		long add(long a, long b) {
			long result = a + b;
			return result;
			// return a + b;		// 위의 두 줄을 한줄로 간단하게 표현 가능
		}
		
		long subtract(long a, long b) {
			return a - b;
			
		}
		
		long multiply(long a, long b) {
			return a * b;
			
		}
		
		double divide(double a, double b) {
			return a / b;
			
		}
}
